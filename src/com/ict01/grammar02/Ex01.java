package com.ict01.grammar02;
class Ex01 {
	public static void main(String[] args){
		//논리형 : boolean , true / false를 저장
		boolean bo1; // 논리형 자료 bo1 선언 - 선언을 하게 되면 같은 이름의 선언은 불가능.
		bo1 = true; // 변수(bo1)에 데이터를 입력
		System.out.println(bo1); //변수 호출 = 변수가 가진 데이터를 내보냄.
		bo1 = false;
		System.out.println(bo1);
		boolean bo2 = true;
		System.out.println(bo2);	
	}
}